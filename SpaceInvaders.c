void PortB_Init(void){ volatile unsigned long delay;
  SYSCTL_RCGC2_R |= 0x00000002;     // 1) F clock
  delay = SYSCTL_RCGC2_R;           // delay   
  GPIO_PORTB_LOCK_R = 0x4C4F434B;   // 2) unlock PortF PF0  
  GPIO_PORTB_CR_R = 0x1F;           // allow changes to PF4-0       
  GPIO_PORTB_AMSEL_R = 0x00;        // 3) disable analog function
  GPIO_PORTB_PCTL_R = 0x00000000;   // 4) GPIO clear bit PCTL  
  GPIO_PORTB_DIR_R = 0x00;          // 5) PF4,PF0 input, PF3,PF2,PF1 output 
  GPIO_PORTB_AFSEL_R = 0x00;        // 6) no alternate function
  GPIO_PORTB_PUR_R = 0xC1;          // enable pullup resistors on PF4,PF0       
  GPIO_PORTB_DEN_R = 0xFF;          // 7) enable digital pins PF4-PF0


	NVIC_EN0_R |= (1<<1);            // enable interrupt at switch 
	EnableInterrupts(); 
	NVIC_PRI0_R  &= ~ (7<<13);        // priority reg 
	NVIC_PRI0_R  |=(5<<13);
	
	GPIO_PORTB_IM_R |= (1<<0)|(1<<6)|(1<<7);
	GPIO_PORTB_IS_R &=~(1<<0)|(1<<6)|(1<<7);
	GPIO_PORTB_IBE_R &=~(1<<0)|(1<<6)|(1<<7);
	GPIO_PORTB_IEV_R &=~(1<<0)|(1<<6)|(1<<7);	
}
void PortF_Init(void){ volatile unsigned long delay;
  SYSCTL_RCGC2_R |= 0x00000020;     // 1) F clock
  delay = SYSCTL_RCGC2_R;           // delay   
  GPIO_PORTF_LOCK_R = 0x4C4F434B;   // 2) unlock PortF PF0  
  GPIO_PORTF_CR_R = 0x1F;           // allow changes to PF4-0       
  GPIO_PORTF_AMSEL_R = 0x00;        // 3) disable analog function
  GPIO_PORTF_PCTL_R = 0x00000000;   // 4) GPIO clear bit PCTL  
  GPIO_PORTF_DIR_R = 0x0E;          // 5) PF4,PF0 input, PF3,PF2,PF1 output   
  GPIO_PORTF_AFSEL_R = 0x00;        // 6) no alternate function
  GPIO_PORTF_PUR_R = 0x11;          // enable pullup resistors on PF4,PF0       
  GPIO_PORTF_DEN_R = 0x1F;          // 7) enable digital pins PF4-PF0  
	
	GPIO_PORTF_IM_R |= (1<<4);        //interrupt mask reg set 1 at reg will recieve interrupt 
	NVIC_EN0_R |= (1<<30);            // enable interrupt at switch 
	EnableInterrupts(); 
	NVIC_PRI7_R  &= ~ (7<<21);        // priority reg 
	NVIC_PRI7_R  |=(5<<21);
	GPIO_PORTF_IS_R &=~(1<<4);
	GPIO_PORTF_IBE_R &=~(1<<4);
	GPIO_PORTF_IEV_R &=~(1<<4);
	
	GPIO_PORTF_IM_R |= (1<<0);
	GPIO_PORTF_IS_R &=~(1<<0);
	GPIO_PORTF_IBE_R &=~(1<<0);
	GPIO_PORTF_IEV_R &=~(1<<0);
	
}
